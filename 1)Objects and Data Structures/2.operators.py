#TAM SAYI BÖLMESİ(//)
#Bu operatör, bir sayının başka bir sayıya bölümünden ortaya çıkan bölüm sonucunu tam sayı vermektedir.

print(4 // 2)
print(13 // 4)
print(22 // 7)
print(40 // 7)
print(22.7 / 11.4)
print(22.7 // 11.4)

print("***********")

#KALANI BULMA(%)
#Bu operatör de, bir sayının başka bir sayıya bölümünden kalan sonucu bulmamızı sağlar.

print(13 % 4)
print(14 % 2)
print(330 % 111)
print(22.7 % 11.4)

print("***********")

#ÜS BULMA(**)
#Bu operatör bir sayının üssünü bulmamızı sağlar.Operatörün solundaki sayının sağındaki sayıya göre üssünü ekrana basar.

#4^3
print(4 ** 3)
print(2 ** 4)
print(3.14 ** 3)

#KAREKÖK bulmak için üssü kesirli yazarız.

#64'ün karekökü
print(64 ** 0.5)
print(8 ** (1/3))

print("**********")

#İşaret Değiştirme(-)

a = 4
print(a)
print(-a)

print("**********")

